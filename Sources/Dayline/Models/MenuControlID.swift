import Foundation

/// Stable identifiers for hoverable menu chrome controls.
enum MenuControlID {
  /// Header refresh button.
  case refresh

  /// Calendar tomorrow disclosure row.
  case tomorrowEvents

  /// Linear issue creation button.
  case newLinearIssue

  /// Linear issue reveal-more row.
  case moreIssues

  /// Linear issue collapse-extra row.
  case fewerIssues

  /// Local note creation button.
  case newNote

  /// Local note reveal-more row.
  case moreNotes

  /// Local note collapse-extra row.
  case fewerNotes

  /// Footer settings button.
  case settings

  /// Footer quit button.
  case quit
}
