import Foundation

/// Stable identifiers for hoverable menu chrome controls.
enum MenuControlID {
  /// Header refresh button.
  case refresh

  /// Calendar tomorrow disclosure row.
  case tomorrowEvents

  /// Linear issue reveal-more row.
  case moreIssues

  /// Linear issue collapse-extra row.
  case fewerIssues

  /// Footer settings button.
  case settings

  /// Footer quit button.
  case quit
}
